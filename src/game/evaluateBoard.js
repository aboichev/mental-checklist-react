const getAbsValue = (piece, isBlack, x, y) => {
    if (piece.type === 'p') {
        return 10 + (isBlack ? pawnEvalBlack[y][x] : pawnEvalWhite[y][x]);
    } else if (piece.type === 'n') {
        return 30 + knightEval[y][x];
    } else if (piece.type === 'b') {
        return 30 + (isBlack ? bishopEvalBlack[y][x] : bishopEvalWhite[y][x]);
    } else if (piece.type === 'r') {
        return 50 + (isBlack ? rookEvalBlack[y][x] : rookEvalWhite[y][x]);
    } else if (piece.type === 'q') {
        return 90 + evalQueen[y][x];
    } else if (piece.type === 'k') {
        return 1000 + (isBlack ? kingEvalBlack[y][x] : kingEvalWhite[y][x]);
    }
    throw new Error("Unknown piece type: " + piece.type);
};

const getPieceValue = (piece, x, y) => {
    if (piece === null) {
        return 0;
    }
    var absValue = getAbsValue(piece, piece.color === 'b', x, y);
    return piece.color === 'w' ? absValue : -absValue;
};
  
export default (board) => {
    var totalEvaluation = 0;
    for (var i = 0; i < 8; i++) {
        for (var j = 0; j < 8; j++) {
            totalEvaluation = totalEvaluation + getPieceValue(board[i][j], i, j);
        }
    }
    return totalEvaluation;
};